let numero1 = Number(prompt(`Qual o primeiro número?`))
let numero2 = Number(prompt(`Qual o segundo número?`))

let soma = numero1 + numero2
let sub = numero1 - numero2
let mult = numero1 * numero2
let divis = numero1 / numero2

alert(
    `Soma: ${soma}
    \nSubtração: ${sub}
    \nMultiplicação: ${mult}
    \nDivisão: ${divis}`
)